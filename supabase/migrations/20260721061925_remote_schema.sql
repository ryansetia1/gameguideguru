CREATE OR REPLACE FUNCTION "public"."match_guide_chunks"("p_guide_urls" "text"[], "p_guide_bundles" "text"[], "p_embedding" "public"."vector", "p_limit" integer DEFAULT 5) RETURNS TABLE("guide_url" "text", "chunk_text" "text", "similarity" double precision)
    LANGUAGE "sql" STABLE
    AS $$
  select
    guide_url,
    chunk_text,
    1 - (embedding <=> p_embedding) as similarity
  from public.guide_chunks
  where
    (cardinality(p_guide_urls) > 0 and guide_url = any(p_guide_urls))
    or (cardinality(p_guide_bundles) > 0 and guide_bundle = any(p_guide_bundles))
  order by embedding <=> p_embedding
  limit p_limit;
$$;


