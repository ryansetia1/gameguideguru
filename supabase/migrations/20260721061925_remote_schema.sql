CREATE OR REPLACE FUNCTION "public"."merge_guide_bundle_cache"("p_bundle_key" "text", "p_new_data" "jsonb") RETURNS "void"
    LANGUAGE "plpgsql"
    AS $$
declare
  existing_data jsonb;
  merged_pages jsonb;
  v_title text;
  v_url text;
  v_is_blocked jsonb;
begin
  select data into existing_data from public.guide_bundle_cache 
    where bundle_key = p_bundle_key for update;
    
  if not found then
    insert into public.guide_bundle_cache (bundle_key, data) 
      values (p_bundle_key, p_new_data)
      on conflict (bundle_key) do update set data = p_new_data, fetched_at = now();
  else
    merged_pages := coalesce(existing_data->'pages', '[]'::jsonb) || coalesce(p_new_data->'pages', '[]'::jsonb);
    
    v_title := coalesce(nullif(p_new_data->>'title', ''), existing_data->>'title');
    v_url := coalesce(nullif(p_new_data->>'canonicalUrl', ''), existing_data->>'canonicalUrl');
    
    -- if p_new_data has isBlocked, use it. Else, keep existing if any.
    if p_new_data ? 'isBlocked' then
      v_is_blocked := jsonb_build_object('isBlocked', p_new_data->'isBlocked');
    elsif existing_data ? 'isBlocked' then
      v_is_blocked := jsonb_build_object('isBlocked', existing_data->'isBlocked');
    else
      v_is_blocked := '{}'::jsonb;
    end if;

    update public.guide_bundle_cache set 
      data = jsonb_build_object(
        'title', v_title,
        'canonicalUrl', v_url,
        'pages', merged_pages
      ) || v_is_blocked,
      fetched_at = now()
    where bundle_key = p_bundle_key;
  end if;
end;
$$;


ALTER FUNCTION "public"."merge_guide_bundle_cache"("p_bundle_key" "text", "p_new_data" "jsonb") OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."rls_auto_enable"() RETURNS "event_trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO 'pg_catalog'
    AS $$
DECLARE
  cmd record;
BEGIN
  FOR cmd IN
    SELECT *
    FROM pg_event_trigger_ddl_commands()
    WHERE command_tag IN ('CREATE TABLE', 'CREATE TABLE AS', 'SELECT INTO')
      AND object_type IN ('table','partitioned table')
  LOOP
     IF cmd.schema_name IS NOT NULL AND cmd.schema_name IN ('public') AND cmd.schema_name NOT IN ('pg_catalog','information_schema') AND cmd.schema_name NOT LIKE 'pg_toast%' AND cmd.schema_name NOT LIKE 'pg_temp%' THEN
      BEGIN
        EXECUTE format('alter table if exists %s enable row level security', cmd.object_identity);
        RAISE LOG 'rls_auto_enable: enabled RLS on %', cmd.object_identity;
      EXCEPTION
        WHEN OTHERS THEN
          RAISE LOG 'rls_auto_enable: failed to enable RLS on %', cmd.object_identity;
      END;
     ELSE
        RAISE LOG 'rls_auto_enable: skip % (either system schema or not in enforced list: %.)', cmd.object_identity, cmd.schema_name;
     END IF;
  END LOOP;
END;
$$;


